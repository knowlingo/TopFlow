"use client"

import { Dialog, DialogContent, DialogHeader, DialogTitle } from "@/components/ui/dialog"
import { GitHubScannerResults } from "@/components/github-scanner-results"
import { Button } from "@/components/ui/button"
import { Badge } from "@/components/ui/badge"
import { Download, Shield } from "lucide-react"
import { toast } from "sonner"

interface GitHubScannerResultsDialogProps {
  open: boolean
  onOpenChange: (open: boolean) => void
  outputs: Record<string, any>
  repository: string
}

export function GitHubScannerResultsDialog({
  open,
  onOpenChange,
  outputs,
  repository
}: GitHubScannerResultsDialogProps) {
  const handleDownloadJSON = () => {
    // Add metadata to JSON export
    const exportData = {
      _meta: {
        generatedBy: "TopFlow",
        url: "https://topflow.dev",
        generatedAt: new Date().toISOString(),
        version: "1.0.0"
      },
      repository,
      ...outputs
    }
    const dataStr = JSON.stringify(exportData, null, 2)
    const dataBlob = new Blob([dataStr], { type: "application/json" })
    const url = URL.createObjectURL(dataBlob)
    const a = document.createElement("a")
    a.href = url
    a.download = `github-security-scan-${repository.replace(/\//g, "-")}-${Date.now()}.json`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
    toast.success("Report downloaded as JSON")
  }

  const handleDownloadMarkdown = () => {
    const scoreData = outputs["calculate-score"] || outputs["score"] || {}
    const aiAnalysis = outputs["ai-analysis"] || ""
    const actionData = outputs["extract-actions"] || {}

    const markdown = `# GitHub Security Scan Report

## Repository: ${repository}

### Security Score: ${scoreData.score || 85}/100 (Grade: ${scoreData.grade || "B+"})

---

## AI Security Analysis

${aiAnalysis}

---

## Priority Recommendations

${actionData.recommendations?.map((rec: any, idx: number) => `
### ${idx + 1}. [${rec.priority}] ${rec.action}

**Impact**: ${rec.impact}
**Effort**: ${rec.effort}
${rec.timeline ? `**Timeline**: ${rec.timeline}` : ""}
`).join("\n") || "No recommendations available"}

---

Generated by [TopFlow](https://topflow.dev) • ${new Date().toLocaleString()}
`

    const dataBlob = new Blob([markdown], { type: "text/markdown" })
    const url = URL.createObjectURL(dataBlob)
    const a = document.createElement("a")
    a.href = url
    a.download = `github-security-scan-${repository.replace(/\//g, "-")}-${Date.now()}.md`
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
    URL.revokeObjectURL(url)
    toast.success("Report downloaded as Markdown")
  }

  // Extract score and grade for header
  const scoreData = outputs["calculate-score"] || outputs["score"] || {}
  const score = scoreData.score || 85
  const grade = scoreData.grade || "B+"

  // Get grade color
  const getGradeColor = (grade: string) => {
    if (grade.startsWith("A")) return "bg-green-500"
    if (grade.startsWith("B")) return "bg-blue-500"
    if (grade.startsWith("C")) return "bg-yellow-500"
    return "bg-red-500"
  }

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent className="max-w-6xl max-h-[90vh] overflow-hidden flex flex-col">
        <DialogHeader className="flex-shrink-0 space-y-3">
          <div className="flex flex-col items-center text-center space-y-2">
            <div className="flex items-center gap-2">
              <Shield className="h-5 w-5 text-primary" />
              <DialogTitle className="text-2xl font-bold">Security Scan Report</DialogTitle>
            </div>
            <div className="flex items-center gap-3 text-muted-foreground">
              <span className="font-medium">{repository}</span>
              <span>•</span>
              <Badge className={`${getGradeColor(grade)} text-white px-3 py-1`}>
                {grade} ({score}/100)
              </Badge>
            </div>
          </div>

          <div className="flex items-center justify-end gap-2 pt-2 border-t">
            <Button onClick={handleDownloadJSON} variant="ghost" size="sm">
              <Download className="h-4 w-4 mr-2" />
              JSON
            </Button>
            <Button onClick={handleDownloadMarkdown} variant="ghost" size="sm">
              <Download className="h-4 w-4 mr-2" />
              Markdown
            </Button>
          </div>
        </DialogHeader>

        <div className="flex-1 overflow-y-auto pr-2">
          <GitHubScannerResults outputs={outputs} repository={repository} />
        </div>

        {/* Footer Badge */}
        <div className="flex-shrink-0 border-t pt-3 pb-1 flex items-center justify-center gap-2 text-xs text-muted-foreground">
          <Shield className="h-3 w-3" />
          <span>Powered by</span>
          <a
            href="https://topflow.dev"
            target="_blank"
            rel="noopener noreferrer"
            className="font-medium text-foreground hover:text-primary transition-colors"
          >
            TopFlow
          </a>
          <span>•</span>
          <span>Built by Former CISO</span>
        </div>
      </DialogContent>
    </Dialog>
  )
}
